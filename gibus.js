// Funzione calcolo Click Zip
function calcolaPrezzoGibusClickZip(modello, larghezza, altezza, tessuto, verniciatura, motore) {
    const db = GIBUS_DATABASE_2025;
    const zip = db.clickZip[modello];
    if (!zip) return { errore: `Modello ${modello} non trovato`, prezzoListino: 0, prezzoNetto: 0 };
    
    // Verifica limiti
    if (larghezza < zip.limiti.Lmin || larghezza > zip.limiti.Lmax ||
altezza < zip.limiti.Hmin || altezza > zip.limiti.Hmax) {
return { errore: `Dimensioni fuori limiti`, prezzoListino: 0, prezzoNetto: 0 };
    }
    
    // Determina tabella prezzi (A per standard, STRUTTURA per TWIN/TWIN_BG)
    const tabella = zip.STRUTTURA ? zip.STRUTTURA : zip.A;
    const isTwin = !!zip.STRUTTURA; // TWIN sono solo struttura, no maggiorazioni tessuto
    
    // Trova indici per interpolazione
    const larghezze = zip.larghezze;
    const altezze = Object.keys(tabella).map(Number).sort((a,b) => a-b);
    
    let l1 = larghezze[0], l2 = larghezze[larghezze.length-1];
    let h1 = altezze[0], h2 = altezze[altezze.length-1];
    
    for (let i = 0; i < larghezze.length - 1; i++) {
if (larghezza >= larghezze[i] && larghezza <= larghezze[i+1]) {
    l1 = larghezze[i]; l2 = larghezze[i+1]; break;
}
    }
    for (let i = 0; i < altezze.length - 1; i++) {
if (altezza >= altezze[i] && altezza <= altezze[i+1]) {
    h1 = altezze[i]; h2 = altezze[i+1]; break;
}
    }
    
    // Interpolazione bilineare
    const idx1 = larghezze.indexOf(l1), idx2 = larghezze.indexOf(l2);
    const p11 = tabella[h1]?.[idx1], p12 = tabella[h1]?.[idx2];
    const p21 = tabella[h2]?.[idx1], p22 = tabella[h2]?.[idx2];
    
    if (!p11 || !p12 || !p21 || !p22) {
return { errore: `Combinazione non disponibile`, prezzoListino: 0, prezzoNetto: 0 };
    }
    
    let prezzoBase;
    if (l1 === l2 && h1 === h2) prezzoBase = p11;
    else if (l1 === l2) prezzoBase = p11 + (p21 - p11) * (altezza - h1) / (h2 - h1);
    else if (h1 === h2) prezzoBase = p11 + (p12 - p11) * (larghezza - l1) / (l2 - l1);
    else {
const pL1 = p11 + (p12 - p11) * (larghezza - l1) / (l2 - l1);
const pL2 = p21 + (p22 - p21) * (larghezza - l1) / (l2 - l1);
prezzoBase = pL1 + (pL2 - pL1) * (altezza - h1) / (h2 - h1);
    }
    
    // Maggiorazioni
    const percTessuto = db.maggiorazioniTessuti[tessuto] || 0;
    let prezzoConTessuto = prezzoBase * (1 + percTessuto / 100);
    
    const maggVern = db.maggiorazioniVerniciature[verniciatura] || { perc: 0, min: 0 };
    let maggiorazioneVern = Math.max(prezzoConTessuto * maggVern.perc / 100, maggVern.min);
    
    let prezzoListino = Math.round(prezzoConTessuto + maggiorazioneVern);
    
    // Motore
    if (motore && db.motorizzazioni[motore]) {
prezzoListino += db.motorizzazioni[motore].prezzo;
    }
    
    const prezzoNetto = Math.round(prezzoListino * (1 - db.info.sconto / 100));
    
    return { modello: zip.nome, prezzoBase: Math.round(prezzoBase), prezzoListino, prezzoNetto, sconto: db.info.sconto };
}

// Funzione calcolo Tenda a Bracci
function calcolaPrezzoGibusTendaBracci(modello, larghezza, sporgenza, bracci, tessuto, verniciatura, motore) {
    const db = GIBUS_DATABASE_2025;
    const tenda = db.tendeBracci[modello];
    if (!tenda) return { errore: `Modello ${modello} non trovato`, prezzoListino: 0, prezzoNetto: 0 };
    
    const tabellaBracci = tenda[`${bracci}_bracci`];
    if (!tabellaBracci) return { errore: `${bracci} bracci non disponibili`, prezzoListino: 0, prezzoNetto: 0 };
    
    // Trova indici
    const larghezze = tenda.larghezze;
    let idxL = larghezze.findIndex(l => larghezza <= l);
    if (idxL === -1) idxL = larghezze.length - 1;
    
    const sporgenze = Object.keys(tabellaBracci).map(Number).sort((a,b) => a-b);
    let sporgenzaKey = sporgenze.find(s => sporgenza <= s) || sporgenze[sporgenze.length-1];
    
    const prezzoBase = tabellaBracci[sporgenzaKey]?.[idxL];
    if (!prezzoBase) return { errore: `Combinazione non disponibile`, prezzoListino: 0, prezzoNetto: 0 };
    
    // Maggiorazioni tende bracci
    const percTessutoBracci = { "Starlight": 0, "Starlight Blue": 7, "Starlight Blue XL": 8, "Acrylic": 0, "Soltis 86": 12, "Soltis 88": 15, "Soltis 92": 15 };
    const percTessuto = percTessutoBracci[tessuto] || 0;
    let prezzoConTessuto = prezzoBase * (1 + percTessuto / 100);
    
    const maggVern = db.maggiorazioniVerniciature[verniciatura] || { perc: 0, min: 0 };
    let maggiorazioneVern = Math.max(prezzoConTessuto * maggVern.perc / 100, maggVern.min);
    
    let prezzoListino = Math.round(prezzoConTessuto + maggiorazioneVern);
    
    if (motore && db.motorizzazioni[motore]) {
prezzoListino += db.motorizzazioni[motore].prezzo;
    }
    
    const prezzoNetto = Math.round(prezzoListino * (1 - db.info.sconto / 100));
    
    return { modello: tenda.nome, prezzoBase, prezzoListino, prezzoNetto, sconto: db.info.sconto };
}

